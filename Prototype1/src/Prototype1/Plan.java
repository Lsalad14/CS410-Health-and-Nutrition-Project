package Prototype1;

public class Plan {

	private String username;
	private int foodid;
	private int count;
	
	public String getUsername() {
		return username;
	}
	
	public void setUsername(String username) {
		this.username = username;
	}
	
	public int getFoodid() {
		return foodid;
	}
	
	public void setFoodid(int foodid) {
		this.foodid = foodid;
	}
	
	public int getCount() {
		return count;
	}
	
	public void setCount(int count) {
		this.count = count;
	}
}
